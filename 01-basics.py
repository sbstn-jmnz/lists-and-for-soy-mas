# Crear una lista vacía
my_list = []

# Una propiedad importante del las listas es su largo

print(len(my_list))

# Agregar elementos a un arreglo o lista

my_list.append(True)
my_list.append("Hola")
my_list.append(34)

print(my_list)

# La principal característica de un arreglo es que sus elementos están ordenados por su posición partiendo desde cero

print(my_list[2])
print(my_list[0])
print(my_list[1])

# Que pasa con lo índices negativos
print(my_list[-1])

print(len(my_list))

# Como conclusión podemos decir que el último indice es su largo menos uno